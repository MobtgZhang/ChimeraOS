/// Built-in system icons for the desktop environment.
/// Each icon is a 16x16 pixel bitmap stored as color indices:
///   0 = transparent, 1 = primary, 2 = secondary, 3 = accent, 4 = highlight

const color_mod = @import("color.zig");
const Color = color_mod.Color;

pub const ICON_SIZE: u32 = 16;

pub const IconId = enum {
    apple_logo,
    finder,
    terminal,
    settings,
    folder,
    file_text,
    info,
    power,
};

pub fn getIcon(id: IconId) *const [ICON_SIZE * ICON_SIZE]u8 {
    return switch (id) {
        .apple_logo => &icon_apple,
        .finder => &icon_finder,
        .terminal => &icon_terminal,
        .settings => &icon_settings,
        .folder => &icon_folder,
        .file_text => &icon_file,
        .info => &icon_info,
        .power => &icon_power,
    };
}

/// Resolve palette index → actual color.
pub fn paletteColor(idx: u8, primary: Color, secondary: Color, accent: Color, highlight: Color) Color {
    return switch (idx) {
        0 => 0, // transparent
        1 => primary,
        2 => secondary,
        3 => accent,
        4 => highlight,
        else => primary,
    };
}

